func writeHTMLEnd(b *strings.Builder) {
	b.WriteString("</body></html>\n")
}

func writeHTMLFindings(b *strings.Builder, reports []checker.Report) {
	hasFindings := false
	for _, report := range reports {
		if len(report.Findings) > 0 {
			hasFindings = true
			break
		}
	}
	if !hasFindings {
		return
	}
	b.WriteString("<section class=\"panel findings\"><div class=\"panel-head\"><h2>Findings</h2></div><table><thead><tr><th>target</th><th>severity</th><th>scope</th><th>message</th></tr></thead><tbody>\n")
	for _, report := range reports {
		for _, finding := range report.Findings {
			b.WriteString("<tr>")
			writeTD(b, report.Target)
			writeTDClass(b, finding.Severity, finding.Severity)
			writeTD(b, finding.Scope)
			writeTD(b, finding.Message)
			b.WriteString("</tr>\n")
		}
	}
	b.WriteString("</tbody></table></section>\n")
}

func writeTD(b *strings.Builder, value string) {
	b.WriteString("<td>")
	b.WriteString(html.EscapeString(emptyDash(value)))
	b.WriteString("</td>")
}

func writeTDClass(b *strings.Builder, class, value string) {
	b.WriteString("<td class=\"")
	b.WriteString(html.EscapeString(class))
	b.WriteString("\">")
	b.WriteString(html.EscapeString(emptyDash(value)))
	b.WriteString("</td>")
}

func firstFinding(findings []checker.Finding) string {
	if len(findings) == 0 {
		return "-"
	}
	return findings[0].Message
}

func formatDays(days int) string {
	if days == 0 {
		return "0 days"
	}
	return formatInt(days) + " days"
}

func formatSummary(matched, total, errors int) string {
	return formatInt(matched) + "/" + formatInt(total) + " matched, " + formatInt(errors) + " failed"
}

func formatInt(v int) string {
	return strconv.Itoa(v)
}

func writeCard(b *strings.Builder, label, value string) {
	b.WriteString("<div class=\"card\"><div class=\"label\">")
	b.WriteString(html.EscapeString(label))
	b.WriteString("</div><div class=\"value\">")
	b.WriteString(html.EscapeString(emptyDash(value)))
	b.WriteString("</div></div>")
}

func reportCounts(reports []checker.Report) (ok, warn, critical int) {
	for _, report := range reports {
		switch report.Status {
		case "critical", "error":
			critical++
		case "warn":
			warn++
		default:
			ok++
		}
	}
	return ok, warn, critical
}

func generatedAt() string {
	return time.Now().Format("2006-01-02 15:04 MST")
}

func runID() string {
	return time.Now().UTC().Format("20060102T150405Z")
}
