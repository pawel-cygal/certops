func writeReportsHTML(path, title string, reports []checker.Report) error {
	var b strings.Builder
	writeHTMLStart(&b, title)
	ok, warn, critical := reportCounts(reports)
	b.WriteString("<main class=\"shell\">\n")
	b.WriteString("<section class=\"hero\"><p class=\"eyebrow\">SysTeam Ops Tools</p><h1>" + html.EscapeString(title) + "</h1><p class=\"sub\">TLS and HTTPS posture report for CI/CD, client evidence, and operational review.</p></section>\n")
	b.WriteString("<section class=\"meta-grid\">")
	writeCard(&b, "targets", formatInt(len(reports)))
	writeCard(&b, "ok", formatInt(ok))
	writeCard(&b, "warn", formatInt(warn))
	writeCard(&b, "critical", formatInt(critical))
	writeCard(&b, "generated at", generatedAt())
	writeCard(&b, "run id", runID())
	b.WriteString("</section>\n")
	b.WriteString("<section class=\"panel\"><div class=\"panel-head\"><h2>Endpoints</h2><span class=\"badge ")
	if critical > 0 {
		b.WriteString("critical\">critical")
	} else if warn > 0 {
		b.WriteString("warn\">warn")
	} else {
		b.WriteString("ok\">ok")
	}
	b.WriteString("</span></div><table><thead><tr><th>target</th><th>status</th><th>expires in</th><th>expires at</th><th>issuer</th><th>tls</th><th>hsts</th><th>findings</th></tr></thead><tbody>\n")
	for _, report := range reports {
		b.WriteString("<tr>")
		writeTD(&b, report.Target)
		writeTDClass(&b, report.Status, report.Status)
		writeTD(&b, formatDays(report.Certificate.DaysRemaining))
		writeTD(&b, prettyTimestamp(report.Certificate.NotAfter))
		writeTD(&b, shortIssuer(report.Certificate.Issuer))
		writeTD(&b, strings.Join(report.TLS.SupportedVersions, ", "))
		writeTD(&b, yesNo(strings.TrimSpace(report.HTTPS.HSTS) != ""))
		writeTD(&b, firstFinding(report.Findings))
		b.WriteString("</tr>\n")
	}
	b.WriteString("</tbody></table>\n")
	b.WriteString("</section>\n")
	writeHTMLFindings(&b, reports)
	b.WriteString("<p class=\"footer\">Generated by certops. JSON/YAML/PROM output remains available for automation.</p>\n")
	b.WriteString("</main>\n")
	writeHTMLEnd(&b)
	return os.WriteFile(path, []byte(b.String()), 0644)
}

func writeVerifyHTML(path string, report verify.Report) error {
	var b strings.Builder
	writeHTMLStart(&b, "certops verify")
	b.WriteString("<main class=\"shell\">\n")
	b.WriteString("<section class=\"hero\"><p class=\"eyebrow\">SysTeam Ops Tools</p><h1>certops verify</h1><p class=\"sub\">TLS expected-state verification report for CI/CD and operational evidence.</p></section>\n")
	b.WriteString("<section class=\"meta-grid\">")
	writeCard(&b, "file", report.File)
	writeCard(&b, "targets", formatInt(report.Total))
	writeCard(&b, "matched", formatInt(report.Matched))
	writeCard(&b, "failed", formatInt(report.Errors))
	writeCard(&b, "generated at", generatedAt())
	writeCard(&b, "run id", runID())
	b.WriteString("</section>\n")
	b.WriteString("<section class=\"panel\"><div class=\"panel-head\"><h2>Checks</h2><span class=\"badge ")
	if report.Errors > 0 {
		b.WriteString("critical\">failed")
	} else {
		b.WriteString("ok\">ok")
	}
	b.WriteString("</span></div><table><thead><tr><th>name</th><th>target</th><th>status</th><th>expires in</th><th>tls</th><th>detail</th></tr></thead><tbody>\n")
	for _, result := range report.Results {
		detail := "-"
		if len(result.Findings) > 0 {
			detail = result.Findings[0].Message
		} else if result.Error != "" {
			detail = result.Error
		}
		b.WriteString("<tr>")
		writeTD(&b, result.Name)
		writeTD(&b, result.Target)
		writeTDClass(&b, result.Status, result.Status)
		writeTD(&b, formatDays(result.Report.Certificate.DaysRemaining))
		writeTD(&b, strings.Join(result.Report.TLS.SupportedVersions, ", "))
		writeTD(&b, detail)
		b.WriteString("</tr>\n")
	}
	b.WriteString("</tbody></table>\n")
	b.WriteString("</section>\n")
	b.WriteString("<p class=\"footer\">Generated by certops. JSON/YAML/PROM output remains available for automation.</p>\n")
	b.WriteString("</main>\n")
	writeHTMLEnd(&b)
	return os.WriteFile(path, []byte(b.String()), 0644)
}

